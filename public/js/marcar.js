//SCRIPT PARA TROCAR O ÍCONE <i> DE UM QUADRADO VAZIO PARA UM ÍCONE DE UM CHECKBOX MARCADO
function checked1(){
    let checkbox = document.getElementById("ck1")
    if(checkbox.checked){
        let square = document.getElementById("square1")
        square.removeAttribute("class")
        square.setAttribute("class", "fa-solid fa-square-check")
    }else{
        let square = document.getElementById("square1")
        square.removeAttribute("class")
        square.setAttribute("class", "fa-regular fa-square")
    }
}

